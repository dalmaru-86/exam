-- 1. 트랜잭션에 대해서 설명해주세요 
-- 트랜잭션이란 데이터베이스의 상태를 변화시키기 위해 수행하는 작업의 논리적 단위입니다. 
-- 데이터 베이스의 상태를 변화시킨다는 말은 SELECT, INSERT, UPDATE, DELETE 등과 같은 
-- 조작어를 사용하는 행동을 의미합니다.
-- 이런 트랜잭션은 상황에 따라 여러 개가 만들어 질 수 있고,
-- 그 각각의 트랜잭션들은 상황에 따라 Commit(저장) 되거나 Rollback(철회) 될 수 있습니다.
